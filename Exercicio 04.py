#Ler os números, efetuar as 04 operações e mostrar os resultados

nota1 = float(input( "Digite o primeiro número:"))
nota2 = float(input( "Digite o segundo número:"))

soma = nota1 + nota2
subtracao = nota1 - nota2
multiplicacao = nota1 * nota2
divisao = nota1 / nota2

print(f" O resultado da sua soma é: {soma}, da subtração é: {subtracao}"
      f",da multiplicação é: {multiplicacao} e da divisão é {divisao}")